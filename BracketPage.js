function showMessage() {
    alert("Please choose your winners from this round. You will then be able to advance to the next round.");
    showRoundButtons();
    let button1 = document.getElementById("startButton");
    button1.style.display = "none";
    words.style.display = "block";
    document.getElementById("number1").innerHTML = "0";
    document.getElementById("number2").innerHTML = "32";
}

function nextRound(){
    checking = checkForFinish();
    if (checking == (32/(2**(round-1)))) {
        let button3 = document.getElementById("nextRoundButton");
        button3.style.display = "none";
        backButton.style.display = "block";
        hideRoundButtons();
        round++;
        if (round == 7){
            window.location.href = "FeedbackPage.html";
        }
        showRoundButtons();
        words.style.display = "block";
        document.getElementById("number1").innerHTML = "0";
        document.getElementById("number2").innerHTML = ((startSpot[round+1] - startSpot[round])/2);
        if (round == 6) {
            document.getElementById("nextRoundButton").innerHTML = "Submit";
        }
    }
}

function checkForFinish(){
    var checking = 0;
    for (let k = startSpot[round]; k < startSpot[round+1]; k++){
        //test = document.getElementById(radioButton[k]);
        if (document.getElementById(radioButton[k]).checked == true) {
            checking++;
        }
    }
    return checking;
}

function showRoundButtons(){
    for (let j = startSpot[round]; j < startSpot[round+1]; j++){
        let test4 = document.getElementById(radioButton[j]);
        test4.style.display = "block";
    }
}

function hideRoundButtons(){
    for (let i = startSpot[round]; i < startSpot[round+1]; i++){
        let test3 = document.getElementById(radioButton[i]);
        test3.style.display = "none";
    }
}

function goBack(){
    let letsDecide = confirm("Are you sure that you want to go back? Your entries in this round will not be saved");
    if (letsDecide) {
        words.style.display = "none";
        for (let h = startSpot[round+1]; h < startSpot[round+2]; h++) {
            let test2 = document.getElementById(texts[h]);
            test2.innerHTML = "";
        }
        hideFromBack();
        round--;
        if (round == 5) {
            document.getElementById("nextRoundButton").innerHTML = "Next Round";
        }
        showRoundButtons();
        let button6 = document.getElementById("nextRoundButton");
        button6.style.display = "block";
        if (round == 1) {
            let button4 = document.getElementById("backButton");
            button4.style.display = "none";
        }
    }
}

function hideFromBack(){
    for (let q = startSpot[round]; q < startSpot[round+1]; q++){
        let test1 = document.getElementById(radioButton[q]);
        test1.style.display = "none";
        document.getElementById(radioButton[q]).checked = false;
    }
}

